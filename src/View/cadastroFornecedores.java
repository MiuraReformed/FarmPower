        tblFornecedores.getColumnModel().getColumn(3).setPreferredWidth(252);
        tblFornecedores.getColumnModel().getColumn(3).setResizable(false);
        tblFornecedores.getTableHeader().setReorderingAllowed(false);
        tblFornecedores.setAutoResizeMode(tblFornecedores.AUTO_RESIZE_OFF);

    private void btnAlterarFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarFornecedorActionPerformed
        // TODO add your handling code here:
        try {
            PreparedStatement pst = conecta.conn.prepareStatement("update fornecedores set nome_fornecedores=?, estado_fornecedores=?, cnpj_fornecedores=? where id_fornecedores=?");
            pst.setString(1, txtFornecedor.getText());
            pst.setString(2, txtFornecedorEstado.getText());
            pst.setString(3, txtCNPJ.getText());
            pst.setInt(4, Integer.parseInt(txtIdFornecedor.getText()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Alterado com sucesso!");
            preencherTabela("select * from fornecedores order by id_fornecedores");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao alterar!!\n ERRO: "+ex);
        }
        txtFornecedor.setText("");
        txtFornecedorEstado.setText("");
        txtCNPJ.setText("");
        txtIdFornecedor.setText("");
    }//GEN-LAST:event_btnAlterarFornecedorActionPerformed


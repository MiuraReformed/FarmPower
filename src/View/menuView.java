    private void btnBuscaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaClienteActionPerformed
        // TODO add your handling code here:
        conecta.conexao();
        try {
            PreparedStatement pst = conecta.conn.prepareStatement("insert into vendas (valor_venda)values(?)");
            pst.setFloat(1, 0);
            pst.execute();
            conecta.executaSQL("select * from vendas");
            conecta.rs.last();
            codVenda = conecta.rs.getInt("id_vendas");
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(rootPane, "ERRO:"+ex);
        }
        flag = 1;
        preencherTabelaCliente("select * from clientes where nome_clientes like '%"+txtNomeClienteVenda.getText()+"%'");
        
    }//GEN-LAST:event_btnBuscaClienteActionPerformed


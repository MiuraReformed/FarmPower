    public void preencherTabelaCliente(String SQL){
        ArrayList dados = new ArrayList();
        
        String [] Colunas = new String[]{"ID","Nome"};
        conecta.conexao();
        conecta.executaSQL(SQL);
        try {
            conecta.rs.first();
            do {            
                dados.add(new Object[]{conecta.rs.getInt("id_clientes"),conecta.rs.getString("nome_clientes")});
        } while (conecta.rs.next());
            
        } catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher o array!!\n ERRO: "+ex);
        }
        
        
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        tblPesquisa.setModel(modelo);
        tblPesquisa.getColumnModel().getColumn(0).setPreferredWidth(100);
        tblPesquisa.getColumnModel().getColumn(0).setResizable(false);
        tblPesquisa.getColumnModel().getColumn(1).setPreferredWidth(590);
        tblPesquisa.getColumnModel().getColumn(1).setResizable(false);
        tblPesquisa.getTableHeader().setReorderingAllowed(false);
        tblPesquisa.setAutoResizeMode(tblPesquisa.AUTO_RESIZE_OFF);
        tblPesquisa.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
       
    }
    

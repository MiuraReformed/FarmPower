    private void tblPesquisaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPesquisaMouseClicked
        // TODO add your handling code here:
        if(flag==1){
            String nome_clientes = "" + tblPesquisa.getValueAt(tblPesquisa.getSelectedRow(),1);
            txtNomeClienteVenda.setText(nome_clientes);
        }else {
            conecta.conexao();
            try {
                String nome_produtos = "" + tblPesquisa.getValueAt(tblPesquisa.getSelectedRow(),1);
                conecta.executaSQL("select * from produtos where nome_produtos='"+nome_produtos+"'");
                conecta.rs.first();
                preco_produto=conecta.rs.getFloat("preco_produtos");
                txtProdutoVenda.setText(nome_produtos);
                txtValorItemVenda.setText(String.valueOf(preco_produto));
                txtQtd.setText("1");
                conecta.desconecta();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao selecionar pre√ßo!!\n\n"+ex);
            }
            
        }
    }//GEN-LAST:event_tblPesquisaMouseClicked


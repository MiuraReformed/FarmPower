    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        
         try {
            PreparedStatement pst = conecta.conn.prepareStatement("update clientes set nome_clientes=?, cpf_clientes=?, endereco_clientes=? where id_clientes=?");
            pst.setString(1, txtNome.getText());
            pst.setString(2, txtCPF.getText());
            pst.setString(3, txtEndereco.getText());
            pst.setInt(4, Integer.parseInt(txtIdClientes.getText()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Alterado com sucesso!");
            preencherTabela("select * from  clientes order by id_clientes");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao alterar!!\n ERRO: "+ex);
        }
        txtIdClientes.setText("");
        txtNome.setText("");
        txtCPF.setText("");
        txtEndereco.setText("");
        btnCadastrar.setEnabled(true);
        
    }//GEN-LAST:event_btnAlterarActionPerformed


    public void preencherTabela(String SQL){
        ArrayList dados = new ArrayList();
        
        String [] Colunas = new String[]{"ID","Nome","CPF","Endereço"};
        
        conecta.executaSQL(SQL);
        try {
            conecta.rs.first();
            do {            
                dados.add(new Object[]{conecta.rs.getInt("id_clientes"),conecta.rs.getString("nome_clientes"),conecta.rs.getString("cpf_clientes"),conecta.rs.getString("endereco_clientes")});
        } while (conecta.rs.next());
            
        } catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher o array!!\n ERRO: "+ex);
        }
        
        
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        tblClientes.setModel(modelo);
        tblClientes.getColumnModel().getColumn(0).setPreferredWidth(50);
        tblClientes.getColumnModel().getColumn(0).setResizable(false);
        tblClientes.getColumnModel().getColumn(1).setPreferredWidth(150);
        tblClientes.getColumnModel().getColumn(1).setResizable(false);
        tblClientes.getColumnModel().getColumn(2).setPreferredWidth(150);
        tblClientes.getColumnModel().getColumn(2).setResizable(false);
        tblClientes.getColumnModel().getColumn(3).setPreferredWidth(286);
        tblClientes.getColumnModel().getColumn(3).setResizable(false);
        tblClientes.getTableHeader().setReorderingAllowed(false);
        tblClientes.setAutoResizeMode(tblClientes.AUTO_RESIZE_OFF);
        tblClientes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
       
    }

    public void preencherTabela(String SQL) {
        ArrayList dados = new ArrayList();
        
        String [] Colunas = new String[]{"ID","Nome","Data","Quantidade","Urgência"};
        
        conecta.executaSQL(SQL);
        try {
            conecta.rs.first();
            
            do {                
                dados.add(new Object[]{conecta.rs.getInt("id_pedidos"),conecta.rs.getString("nome_pedido"),conecta.rs.getString("data_pedido"),conecta.rs.getString("qtd_pedido"),conecta.rs.getString("urgencia_pedido")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher o array!!\n ERRO: "+ex);
        }
        
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        tblPedidos.setModel(modelo);
        tblPedidos.getColumnModel().getColumn(0).setPreferredWidth(50);
        tblPedidos.getColumnModel().getColumn(0).setResizable(false);
        tblPedidos.getColumnModel().getColumn(1).setPreferredWidth(150);
        tblPedidos.getColumnModel().getColumn(1).setResizable(false);
        tblPedidos.getColumnModel().getColumn(2).setPreferredWidth(150);
        tblPedidos.getColumnModel().getColumn(2).setResizable(false);
        tblPedidos.getColumnModel().getColumn(3).setPreferredWidth(150);
        tblPedidos.getColumnModel().getColumn(3).setResizable(false);
        tblPedidos.getTableHeader().setReorderingAllowed(false);
        tblPedidos.setAutoResizeMode(tblPedidos.AUTO_RESIZE_OFF);
        tblPedidos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
    }
    
    

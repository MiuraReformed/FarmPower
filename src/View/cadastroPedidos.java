    private void btnAlterarPedidosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarPedidosActionPerformed
        // TODO add your handling code here:
        try {
            PreparedStatement pst = conecta.conn.prepareStatement("update pedidos set nome_pedido=?, qtd_pedido=?, data_pedido=?,urgencia_pedido=? where id_pedidos=?");
            pst.setString(1, txtPedidoMedicamento.getText());
            pst.setString(2, txtPedidoQuantidade.getText());
            pst.setString(3, txtDataPedido.getText());
            pst.setString(4, txtPedidoUrgencia.getText());
            pst.setInt(5, Integer.parseInt(txtPedidoId.getText()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Alterado com sucesso!");
            preencherTabela("select * from  pedidos order by id_pedidos");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao alterar!!\n ERRO: "+ex);
        }
        txtPedidoMedicamento.setText("");
        txtPedidoQuantidade.setText("");
        txtDataPedido.setText("");
        txtPedidoUrgencia.setText("");
        txtPedidoId.setText("");
        txtPedidoMedicamento.setEnabled(true);
        txtPedidoQuantidade.setEnabled(true);
        txtDataPedido.setEnabled(true);
        txtPedidoUrgencia.setEnabled(true);
        btnAlterarPedidos.setEnabled(false);
        btnExcluirPedido.setEnabled(false);
    }//GEN-LAST:event_btnAlterarPedidosActionPerformed

